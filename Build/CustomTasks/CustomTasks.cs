using System;
using System.IO;
using Microsoft.Build.Framework;
using Microsoft.Build.Utilities;

namespace CustomTasks
{
    public class FormatString : Task
    {
        [Required]
        public string Format { get; set; }

        public string[] Args { get; set; }

        [Output]
        public string Result { get; private set; }

        public override bool Execute()
        {
            Result = string.Format(Format, Args);
            return true;
        }
    }

    public class CurrentTime : Task {
        public string Format { get; set; }

        [Output]
        public string Result { get; private set; }

        public override bool Execute() {
            var now = DateTime.Now;
            Result = string.IsNullOrEmpty(Format) ? now.ToString() : now.ToString(Format);

            return true;
        }
    }

    public class FormatTemplate : Task {
        const string CsHeader = @"/*
 * This code was generated by FormatTemplate as part of the build process. Do not edit it.
 *
 * Generated on {0:yyyy-MM-dd hh:mm:ss}.
 *
 */

";
        
        [Required]
        public ITaskItem TemplateFile { get; set; }

        [Required]
        public ITaskItem OutputFile { get; set; }

        public string[] Parameters { get; set; }
        
        public string FileType { get; set; }

        public override bool Execute() {
            var template = File.ReadAllText(TemplateFile.ItemSpec);

            var type = (FileType ?? "").ToLower();
            string header = "";
            switch(type) {
                case "xml":
                    break;

                default:
                    header = CsHeader;
                    break;
            }

            var result = string.Format(header, DateTime.Now)
                + string.Format(template, Parameters);

            Directory.CreateDirectory(Path.GetDirectoryName(OutputFile.ItemSpec));
            File.WriteAllText(OutputFile.ItemSpec, result);

            return true;
        }
    }

    /// <summary>
    /// Replaces the inline task from StdLib.proj, because xbuild doesn't do inline tasks.
    /// </summary>
    public class CopyStdLib : Task {
        [Required]
        public ITaskItem[] SourceFiles { get; set; }

        [Required]
        public string Destination { get; set; }

        [Required]
        public string BasePath { get; set; }

        public override bool Execute() {
            if (!Directory.Exists(Destination)) {
                Log.LogMessage(MessageImportance.Low, "Creating directory '{0}' ...", Destination);
                Directory.CreateDirectory(Destination);
            }

            foreach (var item in SourceFiles) {
                var dest = item.ItemSpec.Replace(BasePath, Destination);
                if (!Directory.Exists(Path.GetDirectoryName(dest))) {
                    Log.LogMessage(MessageImportance.Low, "Creating directory '{0}' ...", Path.GetDirectoryName(dest));
                    Directory.CreateDirectory(Path.GetDirectoryName(dest));
                }

                Log.LogMessage(MessageImportance.Low, "Copying '{0}' -> '{1}' ...", item.ItemSpec, dest);

                File.Copy(item.ItemSpec, dest, true);
            }

            return true;
        }
    }
}

